#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

/*
** Using a reference in A: exists at the begining and will never change
** Using a pointer in B: does'n exist at the begining and can change
*/

int main(void)
{
	{
		Weapon
		club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.set_type("some other type of club");
		bob.attack();
	}
	{
		Weapon
		club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.set_weapon(club);
		jim.attack();
		club.set_type("some other type of club");
		jim.attack();
	}
	return (0);
}
